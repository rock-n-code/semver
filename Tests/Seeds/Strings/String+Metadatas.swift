extension String.Seeds {
    
    enum Metadatas {
        static let oneZero = "0"
        static let twoZeros = "00"
        static let threeZeros = "000"
        static let twoDigits = "012"
        static let multipleDigits = "123456"
        static let defaultText = "This is a default text."
        static let empty = ""
        static let multipleWhitespaces = "     "
    }
    
}
